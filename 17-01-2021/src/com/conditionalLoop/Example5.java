package com.conditionalLoop;

public class Example5 {

	public static void main(String[] args) {
		/**
		 * initialization;
		 *  
		 *  do
		 *  {
		 *     //print statement
		 *     // increment/decrement statement
		 *     }
		 *    while(condition);
		 *     
		 */
		//initialization
		int a=1;
		do
		{
			System.out.println(a);
			a++;
		}
		while(a<=5);
	}
			
	}


