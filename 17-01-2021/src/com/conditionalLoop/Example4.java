package com.conditionalLoop;

public class Example4 {
//while loop
	public static void main(String[] args) {
		/**
		 * initialization;
		 *  
		 *  while condition
		 *  {
		 *     //print statement
		 *     // increment/decrement statement
		 *     }
		 *     
		 */
		//INITIALIZATION
		int a=1;
		//WHILE CONDITION
		while(a<=4);
		{
			//print statement
			System.out.println(a);
			// increment/decrement statement
			a++;
			
		}
		
		
		
	}
}


		
		

		
		
		
		
		


